// 16_3 String Slice, Join, Concat and includes

// slice()
// split()
// join()
// concat()
// includes()

let country = "I Love Bangladesh";
let expression = "always ";

// slice() methods
console.log(country.slice(0, 2));

// split()

let arr = country.split(" ");
console.log(arr);

// join()
console.log(arr.join(" "));

// concat()
console.log(expression.concat(country));
