// 19-2 Understand the concept of function with visual effects

// function syntax
function funCall() {
  console.log("New function");
}

// function call
funCall();

// =============================

// function return
function returnFun() {
  return console.log("function call");
}

returnFun();

// =============================

// parameter
function add(a, b) {
  console.log(a + b);
}

// argument
add(4, 5);
